/* gestire i cookies */
function setCookie(name, value) {
    const d = new Date();
    d.setTime(d.getTime() + 48 * 60 * 60 * 1000);
    let expiration = "expires" + d.toUTCString();
    document.cookie = name + "=" + value + ";" + expiration + "; path =/";
}
setCookie("My-Data", "lorem");
console.log("Cookies:", document.cookie);

